/* Operadores aritméticos
  = : recebe
  += : valor atual mais...
  -= : Valor atual menos...
  *= : Valor atual multiplicado por... (Pode receber outros operadores aritméticos como: elevado, divídido e outros.)
*/

let num = 10;

num += 5; // Adiciona o valor atual mais 5;
console.log(num)


num -= 2; // Subtraí o valor atual por 2;
console.log(num)


num *= 2; // Multiplica o valor atual por 2;
console.log(num)


num **= 3; // Defina o valor atual na potência de 3.
console.log(num)
