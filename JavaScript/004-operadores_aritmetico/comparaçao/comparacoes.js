/* Operadores de comparação:
    == : igual à
    === : estritamente igual à
    != : diferente de
    !== : estritamente diferente de
    > : maior que 
    < : menor que
    >= : maior ou igual à 
    <= : menor ou igual à
*/

console.log(1 == '1') // '==' apenas verifica se o conteúdo é igual, sem verificar a class/tipo.
console.log(1 === '1') // '===' verifica se o conteúdo e a classe de ambos são iguais, se não forem estritamente iguais, são considerados diferentes.


console.log(1 != '1') // Funciona da mesma forma que o '==', mas verifica se o valor é diferente.
console.log(1 !== '1') // Trabalha igual ao '===', mas consta se os valores são diferentes.
