// Reminder, é um operador aritmético da programação que faz o trabalho de fazer uma divisão com resto.

console.log(6 % 2) // A saída é zero, já que 6 dividido por 2, não resta nenhum valor.

console.log(5 % 2) // Já 5 dividido por 2, resta 1, já que ele não pode ser dívidido por dois, sem ser por número flutuante.



// Com isso, é possível verificar se um valor é ímpar ou par, algo muito importante em alguns momentos.
console.log( 10 % 2 == 0 ) // Verificar se um valor é ìmpar
console.log( 10 % 2 != 0 ) // Verificar se um valor é par
